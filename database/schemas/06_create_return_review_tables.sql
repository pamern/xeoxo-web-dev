--   Tạo các bảng:
--   - Yêu cầu đổi trả
--   - Chi tiết sản phẩm đổi trả
--   - Hoàn tiền
--   - Đánh giá sản phẩm
--   - Media đánh giá
-- DATABASE: PostgreSQL / Supabase
